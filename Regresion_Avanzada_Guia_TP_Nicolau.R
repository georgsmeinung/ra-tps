## ----setup, include=FALSE------------------------------------------------------------------------------
knitr::opts_chunk$set(echo = TRUE)


## ----echo=FALSE, include = FALSE-----------------------------------------------------------------------
# Configurar las opciones de numeración y origen de datos
# y cargar las librerías necesarias para el análisis.

options(scipen=999)
rm(list = ls())

script_path <- file.path(dirname(rstudioapi::getSourceEditorContext()$path), "")
setwd(script_path)

library(readxl)
library(ggplot2)
library(MVN)
library(gridExtra)
library(corrplot)


## ----echo=TRUE-----------------------------------------------------------------------------------------
# Cargar archivo de datos
grasacerdos <- read_excel("grasacerdos.xlsx")
# transformo comas a puntos
grasacerdos <- as.data.frame(lapply(grasacerdos, function(x) as.numeric(gsub(",", ".", x))))

head(grasacerdos)


## ----echo=TRUE-----------------------------------------------------------------------------------------
# Diagrama de dispersión
ggplot(grasacerdos, aes(x = PV, y = EGD)) +
  geom_point() +
  labs(title = "Diagrama de Dispersión",
       x = "Peso Vivo (PV en Kg))",
       y = "Espesor de Grasa Dorsal (EGD en mm)") +
  theme_minimal() 
  


## ----echo=TRUE-----------------------------------------------------------------------------------------
# Coeficiente de correlación muestral
coef_corr <- cor(grasacerdos$PV, grasacerdos$EGD)
print(paste("El coeficiente de correlación muestral es de", round(coef_corr, 2)))


## ----echo=TRUE-----------------------------------------------------------------------------------------
# Prueba de normalidad de las variables con el test de Shapiro-Wilk
# Prueba para PV
shapiro.test(grasacerdos$PV)


## ----echo=TRUE-----------------------------------------------------------------------------------------
# Prueba de normalidad de las variables con el test de Shapiro-Wilk
# Prueba para EGD
shapiro.test(grasacerdos$EGD)


## ----echo=TRUE-----------------------------------------------------------------------------------------
# Coeficiente de correlación de Spearman
coef_corr_spearman <- cor(grasacerdos$PV, grasacerdos$EGD, method = "spearman")
print(paste("El coeficiente de correlación de Spearman es de", round(coef_corr_spearman, 2)))


## ----echo=TRUE-----------------------------------------------------------------------------------------
# Prueba de hipótesis para el coeficiente de correlación de Spearman
cor.test(grasacerdos$PV, grasacerdos$EGD, method = "spearman")


